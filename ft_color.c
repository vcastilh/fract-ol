#include "fractol.h"

int	ft_color(int t)
{
	int	r;
	int	g;
	int	b;

	r = (int)(9 * (1 - t) * pow(t, 3) * 150);
	g = (int)(15 * pow((1 - t), 2) * pow(t, 2) * 255);
	b = (int)(8.5 * pow((1 - t), 3) * t * 80);
	return ((int)t << 24 | r << 16 | g << 8 | b);
}
